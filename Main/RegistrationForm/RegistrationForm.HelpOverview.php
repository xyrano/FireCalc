    <legend>Anmeldungen Übersicht</legend>
    In diesem Formular sehen Sie die Übersicht über die nächsten Wettbewerbe ausgehend vom aktuellen Datum.<br>
    Sind Sie in einer Landkreisgruppe sehen Sie nur die Wettbewerbe Ihres Landkreises, sind Sie zusätzlich einer Feuerwehr zugeordnet sehen Sie nur Ihre eigene Feuerwehr sofern diese bereits angemeldet wurde.<br>
    Klicken Sie auf <u><a href="RegistrationForm.php?do=new">Neu</a></u> um eine Neuanmeldung vorzunehmen.
    <hr>
    Felder:
    <ol>
        <li><b>Wettbewerb</b> - Wettbewerb name</li>
        <li><b>Austragungsort</b> - Ort der Veranstaltung</li>
        <li><b>Datum</b> - Wettbewerbsdatum</li>
        <li><b>Feuerwehr</b> - Die Feuerwehr die bereits verbindlich zugesagt hat.</li>
        <li><b>Status <i>nachstehend</i></b> - Wenn '<i>bereits erfasst</i>' dann wurde die Feuerwehr zum Wettbewerb zugelassen. Falls '<i>noch nicht erfasst</i>' wurde die Feuerwehr dem Wettbewerb noch nicht hinzugefügt.</li>
    </ol>
    <hr>
