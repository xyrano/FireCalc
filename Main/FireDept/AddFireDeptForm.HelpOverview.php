<fieldset>
    <legend>Übersicht über Feuerwehren</legend>
    Diese Übersicht zeigt Feuerwehren zu Ihren zugeordneten Gemeinden sowie den Landkreisen.<br>
    Zudem die Anzahl der JF-Mitglieder die der Feuerwehr zugeordnet sind.<br>
    Sie können hier neue Feuerwehren <a href="AddFireDeptForm.php?do=new">hinzufügen</a>, <a href="AddFireDeptForm.php?do=edit">ändern</a> oder <a href="AddFireDeptForm.php?do=delete">löschen</a>.<br>
    <hr>
    Felder:
    <li><b>ID</b> Eindeutige ID der Feuerwehr.</li>
    <li><b>Landkreis</b> Landkreis die der Feuerwehr über der Gemeinde zugeordnet ist.</li>
    <li><b>Gemeinde</b> Gemeinde die der Feuerwehr zugeordnet ist.</li>
    <li><b>Feuerwehr</b> Die Feuerwehr</li>
    <li><b>Mitglieder</b> Ein errechnetes Feld welches die Anzahl der Feuerwehr hinterlegten Mitglieder zeigt. Siehe auch <a href='../MemberManagement/AddMemberForm.php?do=start&page=0'>Mitglieder</a></li>
    <hr>
    <u>Info - Landkreisgruppe:</u><br>
    Sind Sie einer Landkreisgruppe zugeordnet (zu sehen in der Statusleiste 'Landkreisgruppe') sehen Sie nur die Feuerwehren die dem Landkreis der Gruppe unterliegen.<br>
    <u>Info - Feuerwehr:</u><br>
    Sind Sie einer Feuerwehr zugeordnet (zu sehen in der Statusleiste 'Feuerwehr') sehen Sie nur Ihre Feuerwehr der Sie zugeordnet wurden.
</fieldset>
