    <legend>Neue Feuerwehr hinzufügen</legend>
    Hier können Sie eine neue Feuerwehr hinzufügen. Vorraussetzung dafür ist das Sie den Landkreis und die Gemeinde auswählen.<br>
    Die Gemeinde wird auf Basis des Landkreises vorgefiltert angezeigt.<br>
    Klicken Sie auf die DropDown Liste <b>Landkreis</b> und wählen den Landkreis der Feuerwehr aus.<br>
    Klicken Sie danach auf die DropDown Liste <b>Gemeinde</b> und wählen die Gemeinde der Feuerwehr aus.<br>
    Im Textfeld <b>Feuerwehr</b> tragen Sie dann entsprechend die Feuerwehr ein.<br>
    Klicken Sie dann auf '<u>Speichern</u>' um den Datensatz zu erzeugen, es wird eine Prüfung auf bereits existierende Datensätze gemacht.
    <hr>
    Felder:
    <li><b>Landkreis</b> DropDown List zur Auswahl eines Landkreises, die ID des Landkreises wird dabei gespeichert.</li>
    <li><b>Gemeinde</b> DropDown Liste zur Auswahl einer Gemeinde, die ID der Gemeinde wird dabei gespeichert.</li>
    <li><b>Feuerwehr</b> Textfeld um eine Feuerwehr zu definieren.</li>
    <hr>
    <u>Info - Landkreisgruppe</u><br>
    Sind Sie einer Landkreisgruppe zugeordnet sehen Sie nur die Landkreise die dieser Gruppe hinzugefügt worden sind und können somit nur Feuerwehren für diesen Landkreis hinterlegen.
</fieldset>

