<fieldset>
    <legend>'System' Einstellungen</legend>
    In dieser Übersicht finden Sie Einstellungen die das System an unterschiedlichen Stellen beeinflussen und steuern.<br>
    Ändern Sie eine Einstellungen und klicken danach auf '<u>Speichern</u>' um eine Einstellung Ihren Wünschen anzupassen.<br>
    <hr>
    Felder:
    <ol>
    <li><b>Idle Time</b> - Zeitwert um ein automatischen Logout zu erzwingen. In Abhängigkeit des Zeitformates!</li>
    <li><b>Idle Timeformat</b> - Zeitformat um ein automatischen Logout zu erwzingend. In Abhängigkeit des Zeitwerts!</li>
    <li><b>Pagerefresh</b> - Bei Aktivierung wird die 'Inaktivitätszeit' beim aktualisieren einer Seite mittels 'F5' auf null gesetzt.</li>
    <li><b>Delete Memberfiles automatically</b> - Wenn Sie Mitglieder mittels Importlisten aktualisieren wird die Datei dabei hochgeladen und gespeichert.<br>
    Wählen Sie hier aus ob diese Listen automatisch nach der Verarbeitung gelöscht werden sollen.</li>
    <li><b>Ausweisnr. automatisch erstellen?</b> - Setzen Sie auf Yes um für jedes Mitglied eine Automatische Ausweisnummer zu vergeben.</li>
    <li><b>Hide contest</b> - Aktivieren Sie diese Checkbox um Wettbewerbe die hinter dem aktuellen Datum liegen '<u>auszublenden</u>' - diese werden <b>nicht</b> gelöscht!</li>
    <li><b>Delete Member by age of xx</b> - Wählen Sie hier das Alter der Jugendlichen aus um diese automatisch aus dem System zu entfernen.</li>
    <li><b>Fehlerpunkte vorgabe</b> - Geben Sie hier die Fehlerpunkte für den A-Teil vor.</li>
    <li><b>Wettberwerb Gesamtzeit Unterflurhydrant</b> - Geben Sie hier die Gesamtzeit für den A-Teil in der Kategorie Unterflurhydrant vor.</li>
    <li><b>Wettbewerb Gesamtzeit Offenes Gewässer</b> - Geben Sie hier die Gesamtzeit für den A-Teil in der Kagetegorie Offenes Gewässer vor.</li>
    </ol>
</fieldset>